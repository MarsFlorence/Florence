package florence.client;
/**
 * Enumerator that specifies module types.
 */
public enum Type {
	/**
	 * Module is a plain module.
	 */
	PLAIN,
	/**
	 * Module is a dormitory module.
	 */
	DORMITORY,
	/**
	 * Module is a sanitation module.
	 */
	SANITATION,
	/**
	 * Module is a food and water storage module.
	 */
	FWS,
	/**
	 * Module is a gym and recreation module.
	 */
	GR,
	/**
	 * Module is a canteen module.
	 */
	CANTEEN,
	/**
	 * Module is a power module.
	 */
	POWER,
	/**
	 * Module is a control module.
	 */
	CONTROL,
	/**
	 * Module is a airlock module.
	 */
	AIRLOCK,
	/**
	 * Module is a medical module.
	 */
	MEDICAL	

}